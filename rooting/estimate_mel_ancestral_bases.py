def binary_search(a, x): 
    """
        for checking presnce of x in a long list a
    """
    i = bisect.bisect_left(a, x) 
    if i != len(a) and a[i] == x: 
        return True 
    else: 
        return False

def ID_snps_in_block(mmap,snplist,seq,start,size):
    """
        mmap is a dictionary in which the keys are 0-based positions in the melanogaster sequence
        in the mfa block and the values are positions in Dm6
        snplist is a list of snps in the current chromosome
        seq is the melanogaster sequence in a block 
        start is a 0-based position at the beginning of the sequence that comes from the maf file 

        return a dictionary with 0-based positions in the sequence as keys and the 1-based genome snp position as values

        use binary_search for rapid check of whether or not a position is in a list 

    """
    sd = {}
    j = 1
    for i,b  in enumerate(seq):
        if b != '-':
            gbase = mmap[i]
            if binary_search(snplist,gbase):
                sd[i] = gbase
            gbase += 1
    return sd


def addancnums(focalstr,treestr):
    """
        add internal node values to the treestring
        for the focalstr (e.g. 'D_melanogaster') use a value of 0 
        when the node value base estimates are gotten from the raxml output
        the '0' gets used 
    """
    fi = treestr.find(focalstr) + len(focalstr)
    treel = list(treestr)
    ppos = [i+1 for i, x in enumerate(treel) if x == ')']
    i  = fi
    cp = 0
    while True:
        cp -=  treel[i] == '('
        cp +=  treel[i] == ')'
        if cp == 1:
            break
        i += 1
    fi = i+1
    assert fi in ppos
    ppos.reverse()
    for i in range(len(ppos)):
        if ppos[i] ==fi:
            nodeval = str(0)
        else:
            nodeval = str(i+1)
        treel.insert(ppos[i],nodeval)
    newtreestr= ''.join(treel)
    return newtreestr

def getresultslist(partialresultslist,ancaltprobfile,pf,mafn,blocknum,ac,mapundtoref,mapreftound):
    """
    partialresultslist,ancaltprobfile,pf,mafn,bi,align.components[hsindex],mapundtoref,mapreftound
        now only work with ancaltprobfile
        # partialresults is a list with length equal to the text length for sequences in the block
        # ancrefprobfile is the file for the reference sequence from the current block
        # ancaltprobfile is the file for the sequence with alternate alleles swapped in for the current block
        # mapundtoref is a dictionary with positions in the alignment built by raxml that does not include 
        #     undetermined sites, the values are the positions in the original text 
            
        #     mapundtoref[i] holds the position in the full sequence that corresponds to 
        #     position i in the pruned (no undetermined positions) sequence 
        
        # mapreftound is a dictionary with positions in the original text and values that are positions in the 
        #     alignment built by raxml that does not included undetermined positions

        #     mapreftound[j] holds the position in the pruned sequence that corresponds to
        #     position j in the full sequence     

  
    """
    results = []
    bases = ['A','C','G','T']
    try:
        altlines = open(ancaltprobfile,'r').readlines()
        for i,line in enumerate(altlines):
            if line[0] == '0': # human ancestral node
                alti  = i
                break
        for ui in mapreftound:
            ri = mapreftound[ui]  # corresponding position in the original text
            row = partialresultslist[ui]
            if row != [] :
                altls = list(map(float,altlines[ri+alti].split()[3:])) # probabilities given alt allele
                basecall = bases[altls.index(max(altls))]
                newrow = row + altls + [basecall]

                results.append(newrow)
    except:
        errorinfo = "Error: {}  details:  {} line#: {} ".format(sys.exc_info()[0],sys.exc_info()[1], sys.exc_info()[2].tb_lineno)
        print(errorinfo)
        tempstr = "CALCULATING ANCESTOR PROBABILTY PROBLEM TRY FAIL '{}' in block {} file {} startpos {} sequencelength {}\n".format(errorinfo,blocknum,mafn,ac.start,len(ac.text))
        pf.write(tempstr)
    return results


