def idallgaps(fastareffn):
    """
        generate a list of positions where all of the sequences have a gap symbol
    """
    srs = [sr for sr in SeqIO.parse(fastareffn,'fasta')]
    indels = set(i for i, x in enumerate(list(srs[0].seq)) if x == "-")
    for sr in srs[1:]:
        removeset = set()
        for nd in indels:
            if sr.seq[nd] != '-':
                removeset.add(nd)
        indels.difference_update(removeset)
        if len(indels) == 0:
            break
    allindels = list(indels)
    allindels.sort()
    return allindels
        

def getundmatp(pf,workdir,fastareffn,reducedphyfn):
    """
        generate dictionaries that map between the original ref sequence in the maf file
        and the sequence generated by raxml which is based on removing undetermined positions 

        fastareffn is the name of existing file that holds the original sequences
        
        To build these it is necessary to open the raxml phylip format file, make a fasta file
            then open the fasta file and get the sequence 

        returns mapundtoref and mapreftound

        mapundtoref[i] holds the position in the full sequence that corresponds to 
        position i in the pruned (no undetermined positions) sequence 
        
        mapreftound[j] holds the position in the pruned sequence that corresponds to
        position j in the full sequence 

        pf is the problem file
    """
    #get original sequence
    allindels = idallgaps(fastareffn) # holds positions that are not present in reducedphyfn
    try:
        sp = SeqIO.parse(fastareffn,'fasta')
        ref = next(sp)
        assert ref.description == "D_melanogaster"
        refseq =str(ref.seq).upper()
        assert refseq.count('N') == 0
        olen = len(refseq)
        # make a fasta file out of the raxml alignment file 
        if op.exists(reducedphyfn):
            f = open(reducedphyfn,'r')
            # fw = open(reducedfastafn,'w')
            line = f.readline().strip().split()
            [nseqs,seqlen] = list(map(int,line))
            while True:
                [speciesname, undseq] = f.readline().strip().split()
                if speciesname == "D_melanogaster":
                    break
            assert seqlen + len(allindels) == olen
        else:
            undseq = refseq
        ri = ui = 0
        mapundtoref = {}
        while True:
            if ui in allindels:
                ui += 1
            else:
                mapundtoref[ri] = ui
                ri += 1
                ui += 1
            if ui >= olen:
                break
        mapreftound = dict((v,k) for k,v in mapundtoref.items())
    except:
        errorinfo = "Error: {}  details:  {} line#: {} ".format(sys.exc_info()[0],sys.exc_info()[1], sys.exc_info()[2].tb_lineno)
        print(errorinfo)
        tempstr = "GETUNDMATP PROBLEM TRY FAIL '{}' fastareffn {} reducedphyfn {}\n".format(errorinfo,fastareffn,reducedphyfn)
        pf.write(tempstr)
    return mapundtoref,mapreftound

def dm6map(seq,start,size):
    """
        gmap is a dictionary, keys are 1-based positions in Dm6
        values are 1-based positions in the melanogaster sequence in the mfa block 
        positions that were gaps in the human sequence are not mapped
        mmap is a dictionary in which the keys are positions in the melanogaster sequence
        in the mfa block and the values are positions in Dm6
    """
    gmap ={}
    mmap = {}
    gbase = start+1  #start is 0-based, gbase is 1-based
    j = 1
    for i,b  in enumerate(seq):
        if b != '-':
            gmap[gbase] = i+1
            mmap[i] = gbase
            gbase += 1
    assert len(gmap) == size
    assert len(mmap) == size
    return gmap,mmap


